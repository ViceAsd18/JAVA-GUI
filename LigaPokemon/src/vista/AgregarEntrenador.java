    private JTextField buscarTextoVacio(){
        JTextField TextoVacio = null;
        JTextField campos[] = {txtNombre,txtRegion,txtMedallasGanadas,txtNivelExp};
        
        for(JTextField i: campos){
            if(i.getText().isEmpty()){
                TextoVacio = i;
                return  TextoVacio;
            }
        }
        return  TextoVacio;
    }
    
    
