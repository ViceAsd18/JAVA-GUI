    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String nombre = txtNombre.getText();
        String region = txtRegion.getText();
        String medallasGanadas = txtMedallasGanadas.getText();
        String nivelExp = txtNivelExp.getText();
        
        if(nombre.isEmpty() || region.isEmpty() || medallasGanadas.isEmpty() || nivelExp.isEmpty()){
            helper.showError("Los campos no puede estar vacios");
            return;
        }
        
        if(!(ec.isNumber(medallasGanadas)) || (!(ec.isNumber(nivelExp)))){
            helper.showError("Los campos en rojo tiene que ser numeros");
            lblMedallasGanadas.setForeground(Color.RED);
            lblNivelExp.setForeground(Color.RED);
            return;
        }
        
       ec.agregarEntrenador(nombre, region, Integer.parseInt(medallasGanadas),Integer.parseInt(nivelExp));
       helper.showInformation("Se agrego un nuevo Entrenador");
       lblMedallasGanadas.setForeground(Color.BLACK);
       lblNivelExp.setForeground(Color.BLACK);
    }//GEN-LAST:event_btnAgregarActionPerformed

    
