    private void btnAgregarVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarVehiculoActionPerformed
        String marca = txtMarca.getText();
        String modelo = txtModelo.getText();
        String precio = txtPrecio.getText();
        
        String tipo = String.valueOf(cmbTipo.getSelectedItem());
        String estado = String.valueOf(cmbEstado.getSelectedItem());
        
        int anio = jycAnioSelec.getYear() - 1900,      mes = 1-1,        dia = 1;
        Date anioSeleccionado = new Date(anio,mes,dia);
        
        if(marca.isEmpty() || modelo.isEmpty() || precio.isEmpty() || tipo.isEmpty()){
            helper.showError("Los campos no pueden estar vacios");
            return;
        }else{
        vc.agregarCliente(marca, modelo, anioSeleccionado,tipo,Integer.parseInt(precio),estado);
        helper.showInformation("Se agrego un nuevo Cliente");
        limpiar();            
        }
    }//GEN-LAST:event_btnAgregarVehiculoActionPerformed

    private void limpiar(){
        txtMarca.setText("");
        txtModelo.setText("");
        txtPrecio.setText("");
    }
    
