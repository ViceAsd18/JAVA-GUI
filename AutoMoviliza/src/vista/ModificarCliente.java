    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String rut = txtRutBuscar.getText(),          nombre = txtNombre.getText(),          direccion = txtDireccion.getText();
        String tipoCliente = String.valueOf(cmbTipoCliente.getSelectedItem());
        int numeroContacto = Integer.parseInt(txtNumero.getText());
        
        if(rut.isEmpty() || nombre.isEmpty() || direccion.isEmpty() || txtNumero.getText().isEmpty()){
            helper.showError("Los campos no pueden estar vacios");
            return;
        }
        
        cc.modificarCliente(rut, nombre, numeroContacto, direccion, tipoCliente);
        helper.showInformation("Se modifico el cliente");
        limpiar();
    }//GEN-LAST:event_btnModificarActionPerformed

    
    private void limpiar(){
        txtRutBuscar.setText("");
        txtDireccion.setText("");
        txtNombre.setText("");
        txtNumero.setText("");
    }
    
    
