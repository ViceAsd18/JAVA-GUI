    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Cliente clienteEncontrado = cc.buscarClientePorRut(txtRutBuscar.getText());
        
        if(clienteEncontrado == null){
            helper.showError("No se encontro el Cliente");
            return;
        }
        
        txtDireccion.setText(clienteEncontrado.getDireccion());
        txtNombre.setText(clienteEncontrado.getNombre());
        txtNumero.setText(String.valueOf(clienteEncontrado.getNumeroContacto()));
        
        cmbTipoCliente.setSelectedItem(clienteEncontrado.getTipoCliente());
        
    }//GEN-LAST:event_btnBuscarActionPerformed

